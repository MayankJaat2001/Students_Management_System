    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
     DefaultTableModel model = (DefaultTableModel)jTable1.getModel(); 
     try { 
	Class.forName("com.mysql.cj.jdbc.Driver");
	con=DriverManager.getConnection("jdbc:mysql://localhost:3306/studentrecord","root", "root"); 
        ps=con.prepareStatement("select * from studentrecord;");
	ResultSet rs;
        rs=ps.executeQuery();
        int c=0;
	while (rs.next()) { 
		int rollno = rs.getInt("Roll_No"); 
		String name = rs.getString("Name"); 
		int clss = rs.getInt("Class"); 
		String sec = rs.getString("Section"); 
		String address = rs.getString("Address");
		model.addRow(new Object[] { rollno, name, clss, sec, address });
                c++;
	} 
        if(model.getRowCount()>c){
        for(int i=model.getRowCount()-1;i>=c;i--){
            model.removeRow(i);
        }
        }
	rs.close(); 
	ps.close(); 
	con.close(); 
      } 
      catch (ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(this,"Error In Connectivity"); 
      }
    }//GEN-LAST:event_jButton2ActionPerformed

